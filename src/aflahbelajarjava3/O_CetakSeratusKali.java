package aflahbelajarjava3;

public class O_CetakSeratusKali {
    public static void main(String[] args){
        
        //kode For Loop
        for (int i = 1; i <=100; i++){
            System.out.print(i+" ");
        }
        System.out.println(); //agar tulisan pindah kebawah
        System.out.println(); //agar tulisan pindah kebawah
        
        //deklarasi variable tersendiri
        int j = 1;
        //kode While Loop
        while (j <= 100){
        System.out.print(j+" ");
        j++;
        }
        System.out.println(); //agar tulisan pindah kebawah
        System.out.println(); //agar tulisan pindah kebawah
        
        //deklarasi variable tersendiri
        int k = 1;
        //kode Do While Loop
        do {
            System.out.print(k+" ");
            k++;
        }while (k <= 100);
        System.out.println(); //agar tulisan pindah kebawah
    }
    
}
