package aflahbelajarjava1;

public class E_OperatorRelasi {
    public static void main(String[] args){
        
        int i = 37;
        int j = 13;
        int k = 37;
        
        System.out.println("Nilai Variable.......");
        System.out.println("              i = "+ i);
        System.out.println("              j = "+ j);
        System.out.println("              k = "+ k);
        
        System.out.println("Nilai Lebih Besar....");
        System.out.println("              i > j  = "+ (i > j));
        System.out.println("              j > k  = "+ (j > k));
        System.out.println("              k > i  = "+ (k > i));
        
        System.out.println("Nilai Lebih Besar atau Sama dengan....");
        System.out.println("              i >= j = "+ (i >= j));
        System.out.println("              j >= k = "+ (j >= k));
        System.out.println("              k >= i = "+ (k >= i));
        
        System.out.println("Nilai Lebih Kecil....");
        System.out.println("              i < j  = "+ (i < j));
        System.out.println("              j < k  = "+ (j < k));
        System.out.println("              k < i  = "+ (k < i));
        
        System.out.println("Nilai Lebih Kecil atau Sama dengan....");
        System.out.println("              i <= j = "+ (i <= j));
        System.out.println("              j <= k = "+ (j <= k));
        System.out.println("              k <= i = "+ (k <= i));
        
        System.out.println("Nilai Sama Dengan....");
        System.out.println("              i == j = "+ (i == j));
        System.out.println("              j == k = "+ (j == k));
        System.out.println("              k == i = "+ (k == i));
        
        System.out.println("Nilai Tidak Sama Dengan....");
        System.out.println("              i != j = "+ (i != j));
        System.out.println("              j != k = "+ (j != k));
        System.out.println("              k != i = "+ (k != i));
    }
}
